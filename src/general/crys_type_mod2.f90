  subroutine crys_create(my_crys, structure, c_over_a, hratio_cubic, &
      & hratio_hcp, hratio_bct, hratio_cubic_112, self)

    ! Create a crystal type object

    !---------------------------------------------------------------------------

    ! Arguments:
    ! structure: Crystal type
    ! self: Result, crystal type object
    ! hratio_cubic: Array of fcc and bcc slip system strengths ratios
    ! hratio_hcp: Array of hcp slip system strengths ratios
    ! hratio_bct: Array of bct slip system strengths ratios
    ! c_over_a: Hexagonal c/a ratio
    ! hratio_hcp: Ratio of pyramidal strengths to basal/prismatic

    type(crys_type) :: my_crys
    character(len=4), intent(in) :: structure
    real(rk), intent(in), optional :: hratio_cubic(12)
    real(rk), intent(in), optional :: hratio_hcp(18)
    real(rk), intent(in), optional :: hratio_bct(32)
    real(rk), intent(in), optional :: c_over_a
    real(rk), intent(in), optional :: hratio_cubic_112(12)
    type(crys_type) :: self

    ! Locals:

    real(rk), parameter :: dflt_cratio = 1.0d0

    real(rk) :: cratio

    !---------------------------------------------------------------------------

    nullify (self%schmid_3x3, self%vertices, self%vertices3x3)

    self%structure = structure

    ! Expression is a scalar of type integer, character, or logical.

    select case (structure)

    case ("fcc")
      call schmidtensors(my_crys, structure, self%schmid_3x3, &
          & hratio_cubic=hratio_cubic)

    case ("bcc")
      if (my_crys%g_0_bcc_112 .le. 0.0d0) then
        call schmidtensors(my_crys, structure, self%schmid_3x3, &
            & hratio_cubic=hratio_cubic)

      else if (my_crys%g_0_bcc_112 .gt. 0.0d0) then
        call schmidtensors(my_crys, structure, self%schmid_3x3, &
            & hratio_cubic=hratio_cubic, &
            & hratio_cubic_112=hratio_cubic_112)
      end if

    case ("hcp")
      if (present(c_over_a)) then
        cratio = c_over_a

      else
        cratio = dflt_cratio
      end if

      call schmidtensors(my_crys, structure, self%schmid_3x3, c_over_a=cratio, &
          & hratio_hcp=hratio_hcp)

    case ("bct")
      if (present(c_over_a)) then
        cratio = c_over_a

      else
        cratio = dflt_cratio
      end if

      call schmidtensors(my_crys, structure, self%schmid_3x3, c_over_a=cratio, &
          & hratio_bct=hratio_bct)

    case default
    end select

    self%numslip = size(self%schmid_3x3, dim=3)

    ! Retrieve the single-crystal yield surface vertices for crystal type.
    ! Note: fcc/bcc should be the same. These values are presently hard-coded
    ! from the values in the legacy vertices files from DPLab. - jc

    if (structure .eq. "fcc") then
      ! Initialize self values for fcc.
      self%numvertices = 28
      allocate (self%vertices3x3(3, 3, self%numvertices))

      ! Return the vertices 3X3s from data storage subroutine.
      call get_vertices(structure, self%vertices3x3)

      ! Allocate array for tensor decomposition.
      allocate (self%vertices(5, self%numvertices))
      call tensor3ddecompose(self%vertices3x3, dev=self%vertices)

    else if (structure .eq. "bcc") then
      ! Initialize self values for bcc.
      self%numvertices = 28
      allocate (self%vertices3x3(3, 3, self%numvertices))

      ! Return the vertices 3X3s from data storage subroutine.
      call get_vertices(structure, self%vertices3x3)

      ! Allocate array for tensor decomposition.
      allocate (self%vertices(5, self%numvertices))
      call tensor3ddecompose(self%vertices3x3, dev=self%vertices)

    else if ((structure .eq. "hcp") .or. (structure .eq. "bct")) then
      ! Initialize self values for hcp.
      self%numvertices = 240
      allocate (self%vertices3x3(3, 3, self%numvertices))

      ! Return the vertices 3X3s from data storage subroutine.
      call get_vertices(structure, self%vertices3x3)

      ! Allocate array for tensor decomposition.
      allocate (self%vertices(5, self%numvertices))
      call tensor3ddecompose(self%vertices3x3, dev=self%vertices)

    else
      self%numvertices = 0
    end if

  end subroutine crys_create

  !===========================================================================

