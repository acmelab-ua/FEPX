    ! 111, Miller notation

    real(rk), parameter, dimension(3, 12) :: cub_111 = reshape(source=(/ &
        & 1.0d0, 1.0d0, 1.0d0, &
        & 1.0d0, 1.0d0, 1.0d0, &
        & 1.0d0, 1.0d0, 1.0d0, &
        & 1.0d0, 1.0d0, -1.0d0, &
        & 1.0d0, 1.0d0, -1.0d0, &
        & 1.0d0, 1.0d0, -1.0d0, &
        & 1.0d0, -1.0d0, 1.0d0, &
        & 1.0d0, -1.0d0, 1.0d0, &
        & 1.0d0, -1.0d0, 1.0d0, &
        & 1.0d0, -1.0d0, -1.0d0, &
        & 1.0d0, -1.0d0, -1.0d0, &
        & 1.0d0, -1.0d0, -1.0d0  &
        & /), shape=(/3, 12/))

    ! 211, Miller notation (higher order, not currently utilized)

    real(rk), parameter, dimension(3, 12) :: cub_211 = reshape(source=(/ &
        & -1.0d0, -1.0d0, 2.0d0, &
        & -1.0d0, 2.0d0, -1.0d0, &
        &  2.0d0, -1.0d0, -1.0d0, &
        & -1.0d0, -1.0d0, -2.0d0, &
        & -1.0d0, 2.0d0, 1.0d0, &
        &  2.0d0, -1.0d0, 1.0d0, &
        & -1.0d0, 1.0d0, 2.0d0, &
        & -1.0d0, -2.0d0, -1.0d0, &
        &  2.0d0, 1.0d0, -1.0d0, &
        & -1.0d0, 1.0d0, -2.0d0, &
        & -1.0d0, -2.0d0, 1.0d0, &
        &  2.0d0, 1.0d0, 1.0d0  &
        & /), shape=(/3, 12/))

    ! 123 (a), Miller notation (higher order, not currently utilized)

    real(rk), parameter, dimension(3, 12) :: cub_123a = reshape(source=(/ &
        & -1.0d0, -2.0d0, 3.0d0, &
        & -1.0d0, 3.0d0, -2.0d0, &
        &  3.0d0, -1.0d0, -2.0d0, &
        & -1.0d0, -2.0d0, -3.0d0, &
        & -1.0d0, 3.0d0, 2.0d0, &
        &  3.0d0, -1.0d0, 2.0d0, &
        & -1.0d0, 2.0d0, 3.0d0, &
        & -1.0d0, -3.0d0, -2.0d0, &
        &  3.0d0, 1.0d0, -2.0d0, &
        & -1.0d0, 2.0d0, -3.0d0, &
        & -1.0d0, -3.0d0, 2.0d0, &
        &  3.0d0, 1.0d0, 2.0d0  &
        & /), shape=(/3, 12/))

    ! 123 (b), Miller notation (higher order, not currently utilized)

    real(rk), parameter, dimension(3, 12) :: cub_123b = reshape(source=(/ &
        & -2.0d0, -1.0d0, 3.0d0, &
        & -2.0d0, 3.0d0, -1.0d0, &
        &  3.0d0, -2.0d0, -1.0d0, &
        & -2.0d0, -1.0d0, -3.0d0, &
        & -2.0d0, 3.0d0, 1.0d0, &
        &  3.0d0, -2.0d0, 1.0d0, &
        & -2.0d0, 1.0d0, 3.0d0, &
        & -2.0d0, -3.0d0, -1.0d0, &
        &  3.0d0, 2.0d0, -1.0d0, &
        & -2.0d0, 1.0d0, -3.0d0, &
        & -2.0d0, -3.0d0, 1.0d0, &
        &  3.0d0, 2.0d0, 1.0d0  &
        & /), shape=(/3, 12/))

    ! hcp data

    real(rk) :: hcp_n_c(3, 18) = 0.0d0
    real(rk) :: hcp_d_c(3, 18) = 0.0d0
    real(rk) :: scale = 0.0d0

    ! Plane normals, Miller-Bravais notation

    real(rk), parameter, dimension(4, 18) :: hcp_n = reshape(source=(/ &
        &  0.0d0, 0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, 0.0d0, &
        & -1.0d0, 0.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, -1.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, -1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, 1.0d0, &
        & -1.0d0, 1.0d0, 0.0d0, 1.0d0, &
        & -1.0d0, 1.0d0, 0.0d0, 1.0d0, &
        & -1.0d0, 0.0d0, 1.0d0, 1.0d0, &
        & -1.0d0, 0.0d0, 1.0d0, 1.0d0, &
        &  0.0d0, -1.0d0, 1.0d0, 1.0d0, &
        &  0.0d0, -1.0d0, 1.0d0, 1.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, 1.0d0  &
        & /), shape=(/4, 18/))

    ! Slip directions, Miller-Bravais notation

    real(rk), parameter, dimension(4, 18) :: hcp_d = reshape(source=(/ &
        &  2.0d0, -1.0d0, -1.0d0, 0.0d0, &
        & -1.0d0, 2.0d0, -1.0d0, 0.0d0, &
        & -1.0d0, -1.0d0, 2.0d0, 0.0d0, &
        &  2.0d0, -1.0d0, -1.0d0, 0.0d0, &
        & -1.0d0, 2.0d0, -1.0d0, 0.0d0, &
        & -1.0d0, -1.0d0, 2.0d0, 0.0d0, &
        & -2.0d0, 1.0d0, 1.0d0, 3.0d0, &
        & -1.0d0, -1.0d0, 2.0d0, 3.0d0, &
        & -1.0d0, -1.0d0, 2.0d0, 3.0d0, &
        &  1.0d0, -2.0d0, 1.0d0, 3.0d0, &
        &  1.0d0, -2.0d0, 1.0d0, 3.0d0, &
        &  2.0d0, -1.0d0, -1.0d0, 3.0d0, &
        &  2.0d0, -1.0d0, -1.0d0, 3.0d0, &
        &  1.0d0, 1.0d0, -2.0d0, 3.0d0, &
        &  1.0d0, 1.0d0, -2.0d0, 3.0d0, &
        & -1.0d0, 2.0d0, -1.0d0, 3.0d0, &
        & -1.0d0, 2.0d0, -1.0d0, 3.0d0, &
        & -2.0d0, 1.0d0, 1.0d0, 3.0d0  &
        & /), shape=(/4, 18/))

    ! bct data

    real(rk) :: bct_n_c(3, 32) = 0.0d0
    real(rk) :: bct_d_c(3, 32) = 0.0d0

    ! Plane normals, Miller notation

    real(rk), parameter, dimension(3, 32) :: bct_sn = reshape(source=(/ &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, -1.0d0, &
        &  0.0d0, 1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, &
        &  1.0d0, 2.0d0, 1.0d0, &
        & -1.0d0, 2.0d0, 1.0d0, &
        & -1.0d0, -2.0d0, 1.0d0, &
        &  1.0d0, -2.0d0, 1.0d0, &
        &  2.0d0, 1.0d0, 1.0d0, &
        & -2.0d0, 1.0d0, 1.0d0, &
        & -2.0d0, -1.0d0, 1.0d0, &
        &  2.0d0, -1.0d0, 1.0d0 &
        & /), shape=(/3, 32/))

    ! Slip directions, Miller notation

    real(rk), parameter, dimension(3, 32) :: bct_sd = reshape(source=(/ &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 1.0d0, &
        & -1.0d0, 1.0d0, 1.0d0, &
        &  1.0d0, 1.0d0, 1.0d0, &
        & -1.0d0, -1.0d0, 1.0d0, &
        & -1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, &
        &  1.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, -1.0d0, &
        &  1.0d0, 0.0d0, 0.0d0, &
        &  0.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, 1.0d0, 0.0d0, &
        &  1.0d0, -1.0d0, 0.0d0, &
        &  1.0d0, 0.0d0, -1.0d0, &
        &  1.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, -1.0d0, &
        &  0.0d0, 1.0d0, 1.0d0, &
        & -1.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, 1.0d0, &
        &  1.0d0, 0.0d0, 1.0d0, &
        & -1.0d0, 0.0d0, 1.0d0, &
        &  0.0d0, -1.0d0, 1.0d0, &
        &  0.0d0, -1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, 1.0d0, &
        &  0.0d0, 1.0d0, 1.0d0 &
        & /), shape=(/3, 32/))

    ! Construct Schmid tensors

    select case (structure)

    case ("fcc")
      allocate (schmid(3, 3, 12))
      schmid = 0.0d0

      ! Normalize Cartesian vectors

      do i = 1, 12
        cub_110_c(:, i) = cub_110(:, i)/norm2(cub_110(:, i))
        cub_111_c(:, i) = cub_111(:, i)/norm2(cub_111(:, i))

        schmid(:, :, i) = matmul(reshape(cub_110_c(:, i), (/3, 1/)), &
            & reshape(cub_111_c(:, i), (/1, 3/)))

        ! Scale by relative strengths

        scale = 1.0d0/hratio_cubic(i)
        !schmid(:, :, i) = scale * schmid(:, :, i)
      end do

    case ("bcc")
      if (my_crys%g_0_bcc_112 .lt. 0.0d0) then
        allocate (schmid(3, 3, 12))
        schmid = 0.0d0

        ! Normalize Cartesian vectors

        do i = 1, 12
          cub_110_c(:, i) = cub_110(:, i)/norm2(cub_110(:, i))
          cub_111_c(:, i) = cub_111(:, i)/norm2(cub_111(:, i))

          scale = 1.0d0/hratio_cubic(i)

          schmid(:, :, i) = matmul(reshape(cub_111_c(:, i), (/3, 1/)), &
              & reshape(cub_110_c(:, i), (/1, 3/)))*scale
        end do

      else if (my_crys%g_0_bcc_112 .gt. 0.0d0) then
        allocate (schmid(3, 3, 24))
        schmid = 0.0d0

        ! Normalize Cartesian vectors

        do i = 1, 12
          cub_110_c(:, i) = cub_110(:, i)/norm2(cub_110(:, i))
          cub_111_c(:, i) = cub_111(:, i)/norm2(cub_111(:, i))

          scale = 1.0d0/hratio_cubic(i)

          schmid(:, :, i) = matmul(reshape(cub_111_c(:, i), (/3, 1/)), &
              & reshape(cub_110_c(:, i), (/1, 3/)))*scale
        end do

        do i = 13, 24
          cub_211_c(:, i - 12) = cub_211(:, i - 12)/ &
              & norm2(cub_211(:, i - 12))

          scale = 1.0d0/hratio_cubic_112(i - 12)

          schmid(:, :, i) = matmul(reshape(cub_111_c(:, i), (/3, 1/)), &
              & reshape(cub_211_c(:, i), (/1, 3/)))*scale
        end do
      end if

    case ("hcp")
      allocate (schmid(3, 3, 18))
      schmid = 0.0d0

      ! Convert Miller-Bravais notation to Cartesian (orthonormal)

      hcp_n_c(1, :) = hcp_n(1, :)
      hcp_n_c(2, :) = (2.0d0*hcp_n(2, :) + hcp_n(1, :))/dsqrt(3.0d0)
      hcp_n_c(3, :) = hcp_n(4, :)/c_over_a

      hcp_d_c(1, :) = 1.5d0*hcp_d(1, :)
      hcp_d_c(2, :) = (hcp_d(2, :) + 0.5d0*hcp_d(1, :))*dsqrt(3.0d0)
      hcp_d_c(3, :) = hcp_d(4, :)*c_over_a

      ! Normalize Cartesian vectors

      do i = 1, 18
        hcp_n_c(:, i) = hcp_n_c(:, i)/norm2(hcp_n_c(:, i))
        hcp_d_c(:, i) = hcp_d_c(:, i)/norm2(hcp_d_c(:, i))

        schmid(:, :, i) = matmul(reshape(hcp_d_c(:, i), (/3, 1/)), &
            & reshape(hcp_n_c(:, i), (/1, 3/)))

        ! Scale by relative strengths

        scale = 1.0d0/hratio_hcp(i)
        schmid(:, :, i) = scale*schmid(:, :, i)
      end do

    case ("bct")
      allocate (schmid(3, 3, 32))
      schmid = 0.0d0

      ! Convert Miller notation to Cartesian

      bct_n_c(1, :) = bct_sn(1, :)
      bct_n_c(2, :) = bct_sn(2, :)
      bct_n_c(3, :) = bct_sn(3, :)/c_over_a

      bct_d_c(1, :) = bct_sd(1, :)
      bct_d_c(2, :) = bct_sd(2, :)
      bct_d_c(3, :) = bct_sd(3, :)*c_over_a

      ! Normalize Cartesian vectors, construct Schmid tensors

      do i = 1, 32
        ! Normalize

        bct_n_c(:, i) = bct_n_c(:, i)/norm2(bct_n_c(:, i))
        bct_d_c(:, i) = bct_d_c(:, i)/norm2(bct_d_c(:, i))

        ! d*n^t

        schmid(:, :, i) = matmul(reshape(bct_d_c(:, i), (/3, 1/)), &
            & reshape(bct_n_c(:, i), (/1, 3/)))

        ! Scale by relative strengths

        scale = 1.0d0/hratio_bct(i)
        schmid(:, :, i) = scale*schmid(:, :, i)
      end do

    case default
    end select

  end subroutine schmidtensors

